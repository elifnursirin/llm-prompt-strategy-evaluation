import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('model_points.csv', sep=';')

df.head()

#df_dgr sadece "özetleme" metriğini alıyor
df_dgr = df[df['Metrik'] == 'doğruluk']

# Stratejileri orijinal sırasıyla göstermek için eşsiz sıra alın
stratejiler = df_dgr['Strateji'].unique()

plt.figure(figsize=(10, 6))

model_list = df_dgr['Model'].unique()
markers = ['o', 's', 'D', '*']
linestyles = ['-', '--', '-.', ':']

for i, model in enumerate(model_list):
    veri = df_dgr[df_dgr['Model'] == model]
    veri = veri.set_index('Strateji').loc[stratejiler]
    plt.plot(stratejiler, veri['Doğruluk'],
             marker=markers[i % len(markers)],
             linestyle=linestyles[i % len(linestyles)],
             linewidth=2,
             label=model)

plt.xlabel('Strateji')
plt.ylabel('Doğruluk Puanı')
plt.title('Doğruluk Metriğinde Strateji Bazında Modellerin "Doğruluk" Puanları')
plt.legend(title='Model')
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()

