import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('model_points.csv', sep=';')

df.head()

#df_sc sadece "soru-cevap" metriğini alıyor
df_sc = df[df['Metrik'] == 'soru-cevap']

# Stratejileri orijinal sırasıyla göstermek için eşsiz sıra alın
stratejiler = df_sc['Strateji'].unique()

plt.figure(figsize=(10, 6))

# Her modeli ayrı ayrı çizgi olarak çiz
for model in df_sc['Model'].unique():
    veri = df_sc[df_sc['Model'] == model]
    # Stratejilerin sırası karışmasın diye sıralama
    veri = veri.set_index('Strateji').loc[stratejiler]
    plt.plot(stratejiler, veri['Doğruluk'], marker='o', label=model)

plt.xlabel('Strateji')
plt.ylabel('Doğruluk Puanı')
plt.title('Soru-Cevap Metriğinde Strateji Bazında Modellerin "Doğruluk" Puanları')
plt.legend(title='Model')
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()

