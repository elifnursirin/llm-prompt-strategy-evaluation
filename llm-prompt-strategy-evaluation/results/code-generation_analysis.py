import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('model_points.csv', sep=';')

df.head()

#df_ku sadece "kod üretimi" metriğini alıyor
df_ku = df[df['Metrik'] == 'kod-üretimi']

# Stratejileri orijinal sırasıyla göstermek için eşsiz sıra alın
stratejiler = df_ku['Strateji'].unique()

plt.figure(figsize=(10, 6))

model_list = df_ku['Model'].unique()
markers = ['o', 's', 'D', '*']
linestyles = ['-', '--', '-.', ':']

for i, model in enumerate(model_list):
    veri = df_ku[df_ku['Model'] == model]
    veri = veri.set_index('Strateji').loc[stratejiler]
    plt.plot(stratejiler, veri['Doğruluk'],
             marker=markers[i % len(markers)],
             linestyle=linestyles[i % len(linestyles)],
             linewidth=2,
             label=model)

plt.xlabel('Strateji')
plt.ylabel('Doğruluk Puanı')
plt.title('Kod Üretimi Metriğinde Strateji Bazında Modellerin "Doğruluk" Puanları')
plt.legend(title='Model')
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()

